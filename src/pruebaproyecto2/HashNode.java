public class HashNode {
    private User key;
    private Document value;

    public HashNode(User key, Document value) {
        this.key = key;
        this.value = value;
    }

    public User getKey() {
        return key;
    }


    public Document getValue() {
        return value;
    }
    
    
    
    
}
